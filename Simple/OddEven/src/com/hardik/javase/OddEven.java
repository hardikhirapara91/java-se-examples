package com.hardik.javase;

/**
 * Java Odd and Even Number Example
 * 
 * @author HARDIK
 *
 */
public class OddEven {

	public static void main(String[] args) {

		int no = 123;

		if (no % 2 == 0) {
			System.out.println(no + " number is even.");
		} else {
			System.out.println(no + " number is odd.");
		}
	}
}
